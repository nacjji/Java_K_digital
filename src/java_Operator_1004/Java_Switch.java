package java_Operator_1004;

public class Java_Switch {
    public static void main(String[] args) {
        int x,y;
        x=1;y=2;
        switch(x){
            case 1:
                System.out.println("일");
                int k = 10;
                System.out.println(k);
                break;
            case 2:
                System.out.println("이");
                System.out.println(y);
                break;

        }
    }
}
