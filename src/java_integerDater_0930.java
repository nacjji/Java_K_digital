public class java_integerDater_0930 {
    public static void main(String[] args) {
        int x =10;
        long i_want_money = 2200000000L;
        System.out.println(i_want_money);
        char ch = 'A';
        System.out.println(ch);
        System.out.println(x);
        // 아스키 코드 A = 65
        System.out.println(ch+1);

        double d = 10.23;
        System.out.println(d);
        float f = 12.34f;
        System.out.println(f);
    }
}
